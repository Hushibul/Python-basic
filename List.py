# List or Array
listItem = [1, 5, 8, 78, 22, 44, 65, 0, 1, 9, 77, 22, 33, 22]

# For Pushing or Adding item to List
listItem.append(58)

# For Deleting item to List
listItem.remove(78)

# For Deleting the last item
listItem.pop()

# For Sorting in the list
listItem.sort()

# The Length of the array or list
print(len(listItem))

# Return the count of the item in the list
print(listItem.count(22))

# Copying a list
newListItem = listItem.copy()

#

print(listItem)
