# Creating a Dictionary or an Object in Python
dictionary = {
    "name": "Random",
    "mimic": "All"
}

print(dictionary)

# Printing the keys of the dictionary
print(dictionary.keys())

# Printing the value of the dictionary
print(dictionary.values())

# Clearing the dictionary
dictionary.clear()

# Adding value to dictionary
dictionary["address"] = "Dhaka"
print(dictionary)